void print(double matrix[N][N]) {
   for (int i=0; i<N; i++, printf("\n"))
      for (int j=0; j<N; j++)
         printf("%lf ", matrix[i][j]);
}

void multiply(double mat1[][N], double mat2[][N], double res[][N]) { 
    int i, j, k; 
    for (i = 0; i < N; i++) 
    { 
        for (j = 0; j < N; j++) 
        { 
            res[i][j] = 0; 
            for (k = 0; k < N; k++) 
                res[i][j] += mat1[i][k] * mat2[k][j]; 
        } 
    } 
}

